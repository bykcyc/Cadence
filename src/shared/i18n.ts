/** Map a BCP-47 / system locale string to one of our supported locales. */
export function resolveLocale(setting: LanguageSetting, systemLocale: string): Locale {
  if (setting !== 'system') return setting
  const base = (systemLocale || 'en').toLowerCase().split('-')[0]
  const supported = LOCALES.map((l) => l.code)
  return (supported as string[]).includes(base) ? (base as Locale) : 'en'
}

