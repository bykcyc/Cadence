export function translate(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>
): string {
  let s = messages[locale]?.[key] ?? en[key] ?? key
  if (vars) {
    for (const k of Object.keys(vars)) s = s.replaceAll(`{${k}}`, String(vars[k]))
  }
  return s
}
